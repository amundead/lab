  <script>
    // JavaScript for Theme Switching
    const themeButtons = document.querySelectorAll('[data-bs-theme-value]');
    const body = document.body;

    function applyTheme(theme) {
      body.classList.remove('light-theme', 'dark-theme', 'auto-theme');
      if (theme === 'light') {
        body.classList.add('light-theme');
      } else if (theme === 'dark') {
        body.classList.add('dark-theme');
      }
      localStorage.setItem('theme', theme);
    }

    function initTheme() {
      const savedTheme = localStorage.getItem('theme') || 'auto';
      applyTheme(savedTheme);
      themeButtons.forEach(button => {
        const theme = button.getAttribute('data-bs-theme-value');
        button.classList.toggle('active', theme === savedTheme);
        button.setAttribute('aria-pressed', theme === savedTheme);
      });
    }

    themeButtons.forEach(button => {
      button.addEventListener('click', () => {
        const selectedTheme = button.getAttribute('data-bs-theme-value');
        applyTheme(selectedTheme);
        themeButtons.forEach(btn => {
          const theme = btn.getAttribute('data-bs-theme-value');
          btn.classList.toggle('active', theme === selectedTheme);
          btn.setAttribute('aria-pressed', theme === selectedTheme);
        });
      });
    });

    initTheme();
  </script>
